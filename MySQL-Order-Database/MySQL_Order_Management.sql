-- Reporting queries (examples)
-- 1) Monthly revenue (completed orders only)
SELECT DATE_FORMAT(o.order_date, '%Y-%m') AS month,
       ROUND(SUM(oi.quantity * oi.unit_price), 2) AS revenue
FROM orders o
JOIN order_items oi ON oi.order_id = o.order_id
WHERE o.status = 'Completed'
GROUP BY month
ORDER BY month;

-- 2) Top 10 products by revenue
SELECT p.product_name, p.category,
       ROUND(SUM(oi.quantity * oi.unit_price), 2) AS revenue
FROM order_items oi
JOIN products p ON p.product_id = oi.product_id
JOIN orders o ON o.order_id = oi.order_id
WHERE o.status = 'Completed'
GROUP BY p.product_name, p.category
ORDER BY revenue DESC
LIMIT 10;

-- 3) Repeat customers (2+ completed orders)
SELECT o.customer_id, COUNT(*) AS completed_orders
FROM orders o
WHERE o.status = 'Completed'
GROUP BY o.customer_id
HAVING COUNT(*) >= 2
ORDER BY completed_orders DESC;

-- 4) Unpaid orders list
SELECT o.order_id, o.order_date, o.region, p.payment_status, p.amount
FROM orders o
JOIN payments p ON p.order_id = o.order_id
WHERE p.payment_status = 'Unpaid'
ORDER BY o.order_date DESC;